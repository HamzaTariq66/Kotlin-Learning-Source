


/*

    In Kotlin by default all the classes are public in nature or final in nature that is
    by default you cannot inherit from a particular class to the derived class or the subclass.
    So for that the kotlin provides a special keyword of "OPEN" keyword to inherit all the
    properties of a particular class You have to use the keyword of "OPEN".

    Inheritance :
    Inheritance is an important pillar of OOP(Object Oriented Programming). It is the
    mechanism in kotlin by which one class is allow to inherit the features(fields and methods) of
    another class.
                                    OR
    Inheritance is a mechanism in which one object aquires all the properties from it's
    Parent Class Object.

    Advantages :
    .For Code Reusability
    .For Method Overriding

    Important terminology:

    Super Class:
    The class whose features are inherited is known as super class(or a base class or a parent class).

    Sub Class:
    The class that inherits the other class is known as sub class(or a derived class, extended class,
    or child class). The subclass can add its own fields and methods in addition to the superclass
    fields and method

    ->NOTE:
    In Inheritance The Super Class is by default inherit from class of "any" now this any is
    actually the name of the class that contains few functions . These functions are given :

    "ANY" Class contains functions such as :
    -> equal(): Boolean
    -> hashcode(): Int
    -> toString() : String

    so in kotlin always remember every class you define inside your project has a superclass of
    "any".


    TYPES OF INHERITANCE :
    .Single Inheritance
    .MultiLevel Inheritance
    .Hierarchial Inheritance


     -> OverRiding :
     Overriding means redefining and modifying method inside the derived class or child class.

     In order to override some property and also some method we need to use the keyword of
     "open" and if you are overriding something you need to use the keyword of "override"


    

